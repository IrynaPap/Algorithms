
function duplicateDelete(A) {
    n = A.length;
    for (i = 0; i < n - 1; i++) {
        for (j = 0; j, n - 1; j++) {
            if (A[i] == A[j + 1]) {
                deleteA[j + 1]
            }
        }
    } return A;
}
